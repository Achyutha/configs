# Function which initializes the aliases.
function init() {
    alias zshrc='vim ~/.zshrc'
    alias python='python3'
    alias ipy='ipython'
    alias subl='open /Applications/Sublime\ Text.app'
    alias tmuxconf="vim ~/.tmux.conf"
    alias vimrc="vim ~/.vimrc"
    alias prompt='clear && prompt'
    alias g++='g++ -std=c++11'
    alias ls='exa'
    alias ll='ls -lrt modified'
    # alias grep='rg'
}

init
